public class swaping_3rd_var {
    public static void main(String[] args){
        int firstNum =4;
        int secondNum =6;
        int temp ;
        temp= firstNum;
        firstNum =secondNum;
        secondNum=temp;
        System.out.println("After swap the 1st number is " +firstNum);
        System.out.println("After swap the 2nd number is " +secondNum);
    }
}
